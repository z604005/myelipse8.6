package com.spon.flow.interfaces;

import java.util.Map;

public interface SPON_Flow_Interface {
	
	/**
	 * 呈核
	 * @param user_id
	 * @param condMap
	 */
	public void submit(String user_id, Map condMap);
	
	/**
	 * 核准
	 * @param user_id
	 * @param condMap
	 */
	public void approve(String user_id, Map condMap);
	
	/**
	 * 駁回
	 * @param user_id
	 * @param condMap
	 */
	public void reject(String user_id, Map condMap);
	
	/**
	 * 抽單
	 * @param user_id
	 * @param condMap
	 */
	public void cancel(String user_id, Map condMap);
	
	/**
	 * 作廢
	 * @param user_id
	 * @param condMap
	 */
	public void invalid(String user_id, Map condMap);
	
	
}
