package fr.improve.struts.taglib.layout.exception;

public class StrutsLayoutException extends RuntimeException {

	public StrutsLayoutException() {
		super();
	}

	public StrutsLayoutException(String message, Throwable cause) {
		super(message, cause);
	}

	public StrutsLayoutException(String message) {
		super(message);
	}

	public StrutsLayoutException(Throwable cause) {
		super(cause);
	}

}
