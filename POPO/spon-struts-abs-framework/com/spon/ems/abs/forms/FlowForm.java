package com.spon.ems.abs.forms;

import javax.servlet.http.HttpServletRequest;

import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionMapping;

import com.spon.ems.abs.interfaces.ValidateForm;

/**
 * <Form>思邦科技流程抽象FormBean
 * @version 1.0
 * @created 25-四月-2011 下午 02:26:36
 */
public abstract class FlowForm extends ActionForm implements ValidateForm {

	private String flow_no ;
	private String form_no;
	private String flow_type;
	private String flow_sign_flag;
	
	public FlowForm(){

	}

	public void finalize() throws Throwable {
		super.finalize();
	}

	public void reset(ActionMapping mapping, HttpServletRequest request) {
		// TODO Auto-generated method stub
	}

	public String getFlow_no() {
		return flow_no;
	}

	public void setFlow_no(String flowNo) {
		flow_no = flowNo;
	}

	public String getForm_no() {
		return form_no;
	}

	public void setForm_no(String formNo) {
		form_no = formNo;
	}

	public String getFlow_type() {
		return flow_type;
	}

	public void setFlow_type(String flowType) {
		flow_type = flowType;
	}

	public String getFlow_sign_flag() {
		return flow_sign_flag;
	}

	public void setFlow_sign_flag(String flowSignFlag) {
		flow_sign_flag = flowSignFlag;
	}
	
	
	
	
}
